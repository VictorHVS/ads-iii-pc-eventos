package br.ifpi.entity;

public class TipoEvento {
	
	private int id;
	private String nome;
	
	TipoEvento(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}
